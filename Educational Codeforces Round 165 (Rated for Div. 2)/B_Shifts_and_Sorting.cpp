using namespace std;

using LL = long long;
using PII = pair<int, int>;

void solve() {
    string s;
    cin >> s;
    int ones = 0;
    LL res = 0;
    for (auto c : s) {
        if (c == '1') {
            ones += 1;
        } 
        if (c == '0' && ones) {
            res += 1LL * (ones + 1);
        }
    }
    cout << res << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}