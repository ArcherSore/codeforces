void solve() {
    int n;
    cin >> n;
    vector<int> v(n);
    for (auto &x : v) {
        cin >> x;
    } 
    cout << *max_element(v.begin(), v.end()) - *(min_element(v.begin(), v.end())) << '\n';
}

